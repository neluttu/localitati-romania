<script>
    document.addEventListener('DOMContentLoaded', () => {
        const svg = document.querySelector('svg');
        const circles = svg.querySelectorAll('circle');

        if (!circles.length) return;

        function pulseCircle(circle) {
            if (circle.dataset.animating === '1') return;
            circle.dataset.animating = '1';

            circle.animate(
                [{
                        transform: 'scale(1)',
                        opacity: 1
                    },
                    {
                        transform: 'scale(1.5)',
                        opacity: 0.35
                    },
                    {
                        transform: 'scale(1)',
                        opacity: 1
                    }
                ], {
                    duration: 500 + Math.random() * 700,
                    easing: 'ease-in-out'
                }
            ).onfinish = () => {
                circle.dataset.animating = '0';
            };
        }

        function loop() {
            const index = Math.floor(Math.random() * circles.length);
            pulseCircle(circles[index]);

            // random timing
            setTimeout(loop, 300 + Math.random() * 1400);
        }

        loop();
    });
</script>
